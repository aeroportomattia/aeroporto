package model;

public class Passeggero {

	private String nome;
	 
	private String cognome; 
	
	private String data_di_nascita;
	
	private String sesso;
	
	private String email;
	
	private String password;
	
	private String paese_di_residenza;
	
	private String indrizzo;
	
	private int cap;
	
	private String città;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCognome() {
		return cognome;
	}

	public void setCognome(String cognome) {
		this.cognome = cognome;
	}

	public String getData_di_nascita() {
		return data_di_nascita;
	}

	public void setData_di_nascita(String data_di_nascita) {
		this.data_di_nascita = data_di_nascita;
	}

	public String getSesso() {
		return sesso;
	}

	public void setSesso(String sesso) {
		this.sesso = sesso;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPaese_Stato() {
		return paese_di_residenza;
	}

	public void setPaese_Stato(String paese_di_residenza) {
		this.paese_di_residenza = paese_di_residenza;
	}

	public String getIndrizzo() {
		return indrizzo;
	}

	public void setIndrizzo(String indrizzo) {
		this.indrizzo = indrizzo;
	}

	public int getCap() {
		return cap;
	}

	public void setCap(int cap) {
		this.cap = cap;
	}

	public String getCittà() {
		return città;
	}

	public void setCittà(String città) {
		this.città = città;
	}
	
	
	
}
